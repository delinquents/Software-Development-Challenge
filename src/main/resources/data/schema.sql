CREATE TABLE weather (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    city VARCHAR(255) NOT NULL,
    country VARCHAR(255) NOT NULL,
    temperature NUMERIC(5, 2),
    visibility NUMBER,
    description VARCHAR(255) NOT NULL
);
CREATE TABLE wind (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    speed NUMERIC(5, 2),
    deg NUMBER
    weatherId NUMBER
);
CREATE TABLE coord (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    lon NUMERIC(3, 7),
    lat NUMERIC(3, 7),
);
